#include <stdio.h>
#include <math.h>
int main(int argc, char const *argv[])
{
    printf("%f\n", sqrt(16));   // 4.00000
    printf("%f\n", ceil(1.4));  // 2.000000
    printf("%f\n", floor(1.4)); // 1.000000
    printf("%f\n", pow(2, 3));  // 8.000000
    printf("%d\n", abs(-1));    // 1
    printf("%f\n", cbrt(27));   // 3.000000
    return 0;
}
